package com.imastudio.crudmakanan;

public class User {
    String result;
    String msg;

    public User() {
    }

    public void setResult(String result) {
        this.result = result;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getResult() {
        return result;
    }

    public String getMsg() {
        return msg;
    }
}
